public class CtoF {
    public static void main(String[] args) {
        
        // ค่าอุณหภูมิ
        double celsius = 32;
        
        // แปลงเซลเซียสเป็นฟาเรนไฮต์
        double fahrenheit = (celsius * 9/5) + 32;
        
        // แสดงผลลัพธ์
        System.out.println(celsius + "°C is equal to " + fahrenheit + "°F.");
    }
}